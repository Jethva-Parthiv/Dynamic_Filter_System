CREATE TABLE IF NOT EXISTS cars (
    id SERIAL PRIMARY KEY,
    brand TEXT,
    model TEXT,
    variant TEXT,
    year INT,
    price NUMERIC(12,2),
    mileage INT,
    fuel_type TEXT,
    transmission TEXT,
    color TEXT,
    doors INT,
    seats INT,
    engine_cc INT,
    horsepower INT,
    torque INT,
    drivetrain TEXT,
    body_type TEXT,
    length_mm INT,
    width_mm INT,
    height_mm INT,
    curb_weight_kg INT,
    warranty_years INT,
    emission_class TEXT,
    co2_g_km INT,
    registration_year INT,
    last_service_date DATE,
    service_count INT,
    dealer_city TEXT,
    dealer_state TEXT,
    vin TEXT,
    is_certified BOOLEAN,
    owner_count INT,
    previous_accidents INT,
    listed_date DATE,
    sold_date DATE,
    insurance_group INT,
    interior_material TEXT,
    exterior_material TEXT,
    audio_system TEXT,
    navigation BOOLEAN,
    sunroof BOOLEAN,
    bluetooth BOOLEAN,
    airbags INT,
    abs BOOLEAN,
    traction_control BOOLEAN,
    has_parking_sensors BOOLEAN,
    has_camera BOOLEAN,
    price_usd NUMERIC(12,2),
    score_rating NUMERIC(3,2),
    notes TEXT
);
